#include <stdio.h>


int main()
{
    int array[100], search, i, n;

    printf("Numbers of element : ");
    scanf("%d", &n);
    printf("Enter %d integer numbers : ", n);

    for(i = 0; i < n; i++)
    {
        scanf("%d", &array[i]);
    }

    printf("Enter a number to search : ");
    scanf("%d", &search);

    for(i = 0; i < n; i++)
    {
        if(array[i] == search)
        {
            printf("%d is present at location : %d.", search, i + 1);
            break;
        }
    }

    if(i == n)
    {
        printf("Not found! %d isn't present in the array.\n", search);
    }

    return 0;
}
