#include <stdio.h>


int main()
{
    int array[100], n, i, j, pos, swap;

    printf("Number of elements : ");
    scanf("%d", &n);
    printf("Enter %d integer numbers : ", n);

    for(i = 0; i < n; i++)
    {
        scanf("%d", &array[i]);
    }

    for(i = 0; i < (n - 1); i++)
    {
        pos = i;

        for(j = i + 1; j < n; j++)
        {
            if(array[pos] > array[j])
            {
                pos = j;
            }
        }
        if(pos != i)
        {
            swap = array[i];
            array[i] = array[pos];
            array[pos]= swap;
        }
    }

    printf("Selection Sort result : ");

    for(i = 0; i < n; i++)
    {
        printf("%d ", array[i]);
    }
    printf("\n");

    return 0;
}
